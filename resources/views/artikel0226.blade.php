<h3>Apa itu Framework ? </h3>
<p>Framework adalah kerangka kerja yang digunakan untuk mengembangkan aplikasi berbasis desktop atau aplikasi berbasis website. Dengan menggunakan framework Anda akan lebih mudah untuk membuat aplikasi atau website. Itu karena Anda hanya perlu menyusun komponen-komponen pemrograman yang sudah jadi. Dengan kata lain, Anda tidak perlu membuat berbagai fitur dari awal lagi.</p>
<h3>Fungsi Framewok </h3>
<p>Framework memiliki fungsi utama untuk mempermudah para developer mengembangkan aplikasi dan website terkait struktur MVC (Model View Controller) yang digunakan. Selain itu, ada tiga fungsi framework yang dapat Anda ketahui di bawah ini:
</p>
<h3>1. Program Menjadi Lebih Terstruktur dan Tersusun </h3>
<p>Saat developer mengerjakan pengembangan aplikasi atau website yang besar, maka program yang akan ditulis di dalamnya menjadi semakin banyak. Terkadang, semakin banyaknya program yang ditulis akan menjadikan proses debugging semakin lambat. Selain itu, saat Anda mencari kode program yang error akan semakin sulit karena program yang tidak terstruktur.

Oleh karena itu, dengan adanya framework dapat menjadikan program menjadi lebih terstruktur sehingga Anda dapat dengan mudah menemukan kode yang perlu diperbaiki. Bahkan beberapa jenis framework ada yang menerapkan konsep MVC (Model View Controller) yang akan memudahkan developer untuk memisahkan antara logika dan view.</p>
<h3>2. Praktis untuk Developer</h3>
<p>Saat Anda mengembangkan aplikasi atau website, akan lebih praktis ketika Anda menggunakan framework. Alasannya karena framework sudah menyediakan kode berupa function dan class jadi proses pembuatan software atau aplikasi akan menjadi lebih cepat. Anda cukup memanggil function atau class tersebut ke dalam kode program Anda.</p>
<h3>3. Memiliki Keamanan yang Lebih Unggul</h3>
<p>Selama bertahun-tahun, keamanan untuk aplikasi dan website telah menjadi perhatian utama bagi developer. Beberapa dari developer telah menggunakan segala cara untuk  mengamankan aplikasi dan website yang telah mereka buat.

Setelah framework hadir, developer boleh sedikit tenang soal keamanan program yang telah dibuat. Karena setiap celah keamanan dari framework sudah diidentifikasi oleh 10-100+ developer ahli. Framework juga terus diperbarui versinya untuk menawarkan fitur baru dan menangani bug yang meminimalisir celah keamanan framework.
</p>
<h3>Jenis-Jenis Framework untuk Web Development</h3>
<p>Ada banyak sekali framework yang bisa digunakan untuk pengembangan website. Dimana beda bahasa pemrograman, beda pula jenis framework yang harus Anda gunakan. Berikut daftar lengkapnya:</p>
<h3>1. Framework JavaScript</h3>
<p><ul>
    <li>Vue.js — Walaupun terhitung pemain baru, Vue.js dengan cepat menjadi populer. Selain ringan dengan ukuran hanya 58KB saja, Vue.js juga sangat sederhana dan memiliki keamanan yang baik.</li>
    <li>Angular.js — Sangat direkomendasikan untuk pemula. Terutama bagi Anda yang ingin mengembangkan website dengan konsep Rich Internet Application (RIA) atau Single Page Application (SPA).</li>
    <li>Ember.js — Dikenal mempunyai kecepatan dan performa kodenya yang patut diacungi jempol. Ember.js juga mempunyai banyak plugin yang bisa meningkatkan kinerja lebih baik lagi.</li>
    <li>Node.js — Framework yang memungkinan Anda menjalankan JavaScript dari server. Node.js memiliki pustaka server sendiri sehingga Anda tak perlu menggunakan program web server dari luar.</li>
</ul></p>
<h3>2. Framework CSS</h3>
    <p><ul>
        <li>Bootstrap — Merupakan framework CSS terpopuler. Menawarkan segudang fungsi dengan elemen desain yang responsif. Plus, Bootstrap lah yang pertama kali mempunyai filosofi “Mobile-First.”</li>
        <li>Foundation — Menggunakan sistem open-source berbasis SASS, Foundation menawarkan elemen yang fleksibel, modern, dan kaya akan kustomisasi. Foundation juga menyediakan template siap pakai untuk Anda.</li>
        <li>Bulma — Penggunaannya mudah, open-source, responsif, dan bisa bekerja optimal di berbagai perangkat. Jadi, tak heran walaupun Bulma pendatang baru, tapi sudah cukup populer.</li>
        <li>Semantic UI — Fokus kepada penggunaan human-friendly HTML, sehingga memudahkan Anda. Selain itu, menyediakan 3000+ variabel tema dengan 50.000+ komponen UI.
        </li>
        <li>Dan banyak lainnya</li>
    </ul></p>
<h3>3. Framework PHP</h3>
<p><ul>
    <li>Laravel — Framework PHP terpopuler di dunia. Laravel mempunyai sintaks yang elegan, rapi, dan ringkas. </li>
    <li>CodeIgniter — Sangat cocok bagi Anda yang baru belajar PHP framework. Sebab, CodeIgniter mudah dipahami dan mempunyai dokumentasi lengkap.</li>
    <li>Symfony — Menawarkan fitur bundle dan komponen. Sehingga Anda bisa mengambil sebagian fungsi PHP, atau keseluruhan secara langsung.</li>
    <li>Yii — Mempunyai konfigurasi cukup mudah dengan performa dan tingkat keamanan yang baik.</li>
    <li>Zend — Cocok digunakan bagi Anda yang sudah mengerti dasar-dasar PHP. Sebab, Zend digunakan untuk pengembangan website enterprise yang kompleks.</li>
    <li>Dan banyak lainnya</li>
</ul></p>
<h3>Sudah Mengerti Apa itu Framework ?</h3>
<p>Framework adalah platform yang tepat untuk membuat website atau aplikasi yang responsif dan lebih menarik. Dengan bantuan framework, Anda bisa membuat website atau aplikasi dengan lebih cepat dan aman.

Di artikel ini, kami sudah membahas banyak pilihan jenis framework yang dapat Anda gunakan. Anda dapat memilih framework sesuai dengan kebutuhan Anda. Terlebih jika nantinya Anda ingin menjadi web developer, maka pengetahuan framework ini wajib Anda ketahui.

Semoga artikel ini berguna bagi Anda. </p>



